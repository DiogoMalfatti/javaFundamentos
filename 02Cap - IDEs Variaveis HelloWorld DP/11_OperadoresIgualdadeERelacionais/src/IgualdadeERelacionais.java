
public class IgualdadeERelacionais {

	public static void main(String[] args) {
		
		int idade = 10;
		boolean maioridade = idade > 18;
		System.out.println(maioridade);
		
		idade = 20;
		maioridade = idade > 18;
		System.out.println(maioridade);
		
		idade = 20;
		maioridade = idade == 18;
		System.out.println(maioridade);
		
		idade = 20;
		maioridade = idade != 18;
		System.out.println(maioridade);

	}

}
