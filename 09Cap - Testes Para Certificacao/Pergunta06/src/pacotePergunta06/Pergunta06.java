public class Pergunta06 {

	public static void main(String[] args) {
		
		int x = 100;
		int y = 101;
		
		if (x > y || y > x) {
			
			System.out.println(1);
			
		} else if (x > 10 && y > 100) {
			
			System.out.println(2);
			
		} else {
			
			System.out.println(3);
			
		}

	}

}
